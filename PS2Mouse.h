#ifndef PS2_MOUSE_H
#define PS2_MOUSE_H

/*
 * PS2Mouse.h — драйвер PS/2-мыши (IRQ12) для FEXOS.
 *
 * Гораздо проще и надёжнее в QEMU, чем USB 2.0 (EHCI требует контроллер,
 * enumeration, control/interrupt transfers, HID report parsing — это
 * отдельная большая подсистема). PS/2-мышь эмулируется QEMU "из коробки"
 * (i8042), не требует драйверов хост-контроллера и работает через один
 * простой IRQ.
 *
 * Вызывать ПОСЛЕ interrupt_init() (нужен IDT/IRQ12) и ПОСЛЕ ui_extra_init()
 * (нужен ui_set_mouse_pos). Двигает курсор через ui_set_mouse_pos(),
 * отрисовка — как и раньше, через ui_tick() на каждом таймерном тике.
 */

/* Возвращает 0 при успехе (мышь обнаружена и включена),
 * -1 если контроллер/мышь не ответили (в этом случае курсор просто
 * останется неподвижным — не фатально). */
int ps2_mouse_init(void);

/* Текущее состояние кнопок (бит0=левая,1=правая,2=средняя). */
uint8_t ps2_mouse_buttons(void);

#endif /* PS2_MOUSE_H */
