/*
 * ui_extra.h — курсор (PNG, с альфа-каналом) и "тикающие" часы для FEXOS
 *
 * Использование:
 *   1. После fb_init() и монтирования VFS вызвать ui_extra_init().
 *      Загружает UI/cursor.png (RGBA) в память один раз.
 *   2. ui_set_mouse_pos(x, y) — вызывать из обработчика мыши при движении.
 *   3. ui_tick() — вызывать раз в таймерный тик планировщика (см. Scheduler.c).
 *      Внутри: раз в секунду перерисовывает только область часов,
 *      и при каждом тике перерисовывает курсор в новой позиции
 *      (с восстановлением фона из-под старой позиции).
 */
#ifndef UI_EXTRA_H
#define UI_EXTRA_H

#include <stdint.h>

/* Загружает UI/cursor.png. Возвращает 0 при успехе.
 * Если файл не найден/не декодируется — используется fallback-стрелка,
 * рисуемая примитивами (см. ui_extra.c), 0 всё равно возвращается. */
int ui_extra_init(void);

/* Текущая позиция курсора (top-left угла картинки курсора) */
void ui_set_mouse_pos(int32_t x, int32_t y);

/* Вызывать из таймерного тика (ожидается ~100 Гц / SCHED_TIMESLICE).
 * Сама определяет, когда обновлять часы (1 раз/сек) и курсор. */
void ui_tick(void);

#endif /* UI_EXTRA_H */
